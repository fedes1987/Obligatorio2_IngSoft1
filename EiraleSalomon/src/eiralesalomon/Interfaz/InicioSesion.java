    private void btnIniciarSesionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarSesionActionPerformed
        // TODO add your handling code here:
        try{
            String alias = this.txtNombreUsuario.getText();
            
            if (sistema.getListaAlias().contains(alias)) {
                PantallaInicial iPI = new PantallaInicial(sistema);
                iPI.setExtendedState(iPI.MAXIMIZED_BOTH);
                iPI.setVisible(true);
                
                this.dispose();
            }else{                 
                this.lblInfo.setText("Nombre de usuario incorrecto, intenta con uno v치lido.");               
            }
        }catch(Exception e){
            this.lblInfo.setText(e.toString());
        }
    }//GEN-LAST:event_btnIniciarSesionActionPerformed

